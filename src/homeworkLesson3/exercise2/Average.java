package homeworkLesson3.exercise2;

public class Average {

    public static void main(String[] args) {

        //Variables

        int a = 0;

        int b = 1;

        int c = 2;

        int d = 3;

        int e = 4;

        int f = 5;

        int g = 6;

        int h = 7;

        int i = 8;

        int j = 9;

        //calculation

        int average = (a + b + c + d + e + f + g + h + i + j) / 10;

        int divRmainder = (a + b + c + d + e + f + g + h + i + j) % 10;

        // Result output

        System.out.println("Average of the numbers " + average);

        System.out.println("division remainder " + divRmainder);
    }
}
